#include "main.h"
#include <stdio.h>

/**
 * add - Add two operands
 * @op1: first oprand
 * @op2: second oprand
 * Return: an integer value
 */
int add(int op1, int op2)
{
	return (op1 + op2);
}

/**
 * sub - Subtract two operands
 * @op1: first oprand
 * @op2: second oprand
 * Return: an integer value
 */
int sub(int op1, int op2)
{
	return (op1 - op2);
}

/**
 * div - Divide two oprands 
 * @op1: first oprand
 * @op2: second oprand
 * Return: an integer value
 */
int div(int op1, int op2)
{
	return (op1 / op2);
}

/**
 * mod - Modulus of two operands
 * @op1: first oprand
 * @op2: second oprand
 * Return: an integer value
 */
int mod(int op1, int op2)
{
	return (op1 % op2);
}

/**
 * mul - Multiply two operands
 * @op1: first oprand
 * @op2: second oprand
 * Return: an integer value
 */
int mul(int op1, int op2)
{
	return (op1 * op2);
}

